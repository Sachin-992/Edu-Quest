export function getStableUuid(str: string): string {
  let h1 = 0, h2 = 0, h3 = 0, h4 = 0;
  for (let i = 0; i < str.length; i++) {
    const c = str.charCodeAt(i);
    h1 = (h1 * 31 + c) | 0;
    h2 = (h2 * 37 + c) | 0;
    h3 = (h3 * 41 + c) | 0;
    h4 = (h4 * 43 + c) | 0;
  }
  const h = (n: number) => (n >>> 0).toString(16).padStart(8, '0');
  const x = h(h1) + h(h2) + h(h3) + h(h4);
  return `${x.slice(0, 8)}-\
${x.slice(8, 12)}-\
${x.slice(12, 16)}-\
${x.slice(16, 20)}-\
${x.slice(20)}`;
}

